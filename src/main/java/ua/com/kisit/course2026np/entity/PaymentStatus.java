package ua.com.kisit.course2026np.entity;

/**
 * Статуси платежу
 */
public enum PaymentStatus {
    /**
     * В обробці - платіж очікує підтвердження
     */
    PENDING,

    /**
     * Завершено - платіж успішно виконано
     */
    COMPLETED,

    /**
     * Помилка - платіж не виконано через помилку
     */
    FAILED,

    /**
     * Скасовано - платіж скасовано користувачем або системою
     */
    CANCELLED
}